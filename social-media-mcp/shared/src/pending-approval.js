import { writeFile, mkdir } from 'fs/promises';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { existsSync } from 'fs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

/**
 * Pending Approval Output Generator
 * Generates markdown files for social media posts before publishing
 */
export class PendingApprovalGenerator {
  constructor(outputDir) {
    this.outputDir = outputDir || join(__dirname, '..', '..', 'Pending_Approval');
  }

  /**
   * Ensure output directory exists
   */
  async ensureDirectory() {
    if (!existsSync(this.outputDir)) {
      await mkdir(this.outputDir, { recursive: true });
    }
  }

  /**
   * Generate timestamp for filename
   */
  generateTimestamp() {
    const now = new Date();
    return now.toISOString().replace(/[:.]/g, '-').slice(0, -5);
  }

  /**
   * Generate markdown for Facebook post draft
   */
  async generateFacebookDraft(postData, options = {}) {
    await this.ensureDirectory();

    const timestamp = this.generateTimestamp();
    const filename = `FB_POST_${timestamp}.md`;
    const filepath = join(this.outputDir, filename);

    const {
      message,
      link,
      picture,
      place,
      tags,
      scheduledTime,
      targetAudience,
      campaign
    } = postData;

    const content = `# 📘 Facebook Post - Pending Approval

**Generated:** ${new Date().toISOString()}
**Status:** ⏳ Awaiting Approval
**Platform:** Facebook

---

## Post Content

${message ? `
### Message

${message}

` : ''}

${link ? `
### Link

${link}

` : ''}

${picture ? `
### Image/Media

${picture}

` : ''}

${place ? `
### Location

${place}

` : ''}

${tags && tags.length > 0 ? `
### Tags

${tags.map(t => `- ${t}`).join('\n')}

` : ''}

---

## Post Details

| Field | Value |
|-------|-------|
| Scheduled Time | ${scheduledTime || 'Immediate'} |
| Target Audience | ${targetAudience || 'Public'} |
| Campaign | ${campaign || 'N/A'} |
| Post Type | ${link ? 'Link Post' : picture ? 'Photo Post' : 'Text Post'} |

---

## Character Count

| Metric | Count | Limit |
|--------|-------|-------|
| Message | ${message?.length || 0} | 63,206 |
| ${link ? 'Link' : 'N/A'} | ${link ? '1' : '0'} | Unlimited |

---

## Preview

> ${message?.substring(0, 200) || 'No message'}${message?.length > 200 ? '...' : ''}
${link ? `\n🔗 ${link}` : ''}

---

## Approval Required

⚠️ **This post will be saved as draft** on Facebook and will require manual review before publishing.

### Checklist

- [ ] Review content for accuracy
- [ ] Check links are working
- [ ] Verify images/media display correctly
- [ ] Confirm target audience settings
- [ ] Approve for publishing

---

## Execution Details

- **Operation:** Create Facebook Draft Post
- **API:** Facebook Graph API v19.0
- **Endpoint:** /{page-id}/feed
- **State:** draft (unpublished)

---

*Generated by Social Media MCP Server*
`;

    await writeFile(filepath, content, 'utf-8');

    return {
      filepath,
      filename,
      platform: 'facebook',
      type: 'post_draft'
    };
  }

  /**
   * Generate markdown for Instagram post draft
   */
  async generateInstagramDraft(postData, options = {}) {
    await this.ensureDirectory();

    const timestamp = this.generateTimestamp();
    const filename = `IG_POST_${timestamp}.md`;
    const filepath = join(this.outputDir, filename);

    const {
      caption,
      mediaType,
      mediaUrl,
      location,
      hashtags,
      taggedUsers,
      scheduledTime,
      isReel,
      isStory
    } = postData;

    const mediaTypeDisplay = isReel ? '🎬 Reel' : isStory ? '📱 Story' : '📷 Post';

    const content = `# 📷 Instagram ${mediaTypeDisplay} - Pending Approval

**Generated:** ${new Date().toISOString()}
**Status:** ⏳ Awaiting Approval
**Platform:** Instagram

---

## Post Content

### Caption

${caption || '*No caption*'}

${hashtags && hashtags.length > 0 ? `
### Hashtags

${hashtags.map(h => `#${h}`).join(' ')}

` : ''}

${taggedUsers && taggedUsers.length > 0 ? `
### Tagged Users

${taggedUsers.map(u => `- @${u}`).join('\n')}

` : ''}

---

## Media Details

| Field | Value |
|-------|-------|
| Media Type | ${mediaType || 'IMAGE'} |
| Media URL | ${mediaUrl || 'N/A'} |
| Location | ${location || 'None'} |
| Scheduled Time | ${scheduledTime || 'Immediate'} |

---

## Character Count

| Metric | Count | Limit |
|--------|-------|-------|
| Caption | ${caption?.length || 0} | 2,200 |
| Hashtags | ${hashtags?.length || 0} | 30 max |

---

## Preview

${mediaUrl ? `![Media Preview](${mediaUrl})` : '*No media*'}

> ${caption?.substring(0, 125) || 'No caption'}${caption?.length > 125 ? '...' : ''}
${hashtags?.slice(0, 5).map(h => `#${h}`).join(' ') || ''}

---

## Approval Required

⚠️ **This post will be prepared for Instagram** and will require manual review before publishing.

### Checklist

- [ ] Review caption for accuracy and tone
- [ ] Verify media displays correctly
- [ ] Check hashtags are relevant
- [ ] Confirm tagged users are correct
- [ ] Approve for publishing

---

## Execution Details

- **Operation:** Create Instagram Draft Post
- **API:** Instagram Graph API v19.0
- **Media Type:** ${mediaTypeDisplay}
- **State:** draft

---

*Generated by Social Media MCP Server*
`;

    await writeFile(filepath, content, 'utf-8');

    return {
      filepath,
      filename,
      platform: 'instagram',
      type: isReel ? 'reel_draft' : isStory ? 'story_draft' : 'post_draft'
    };
  }

  /**
   * Generate markdown for Twitter/X post draft
   */
  async generateTwitterDraft(postData, options = {}) {
    await this.ensureDirectory();

    const timestamp = this.generateTimestamp();
    const filename = `TW_POST_${timestamp}.md`;
    const filepath = join(this.outputDir, filename);

    const {
      text,
      mediaUrls,
      poll,
      replySettings,
      scheduledTime,
      isThread
    } = postData;

    const content = `# 🐦 Twitter/X Post - Pending Approval

**Generated:** ${new Date().toISOString()}
**Status:** ⏳ Awaiting Approval
**Platform:** Twitter/X

---

## Tweet Content

### Text

${text || '*No text*'}

${mediaUrls && mediaUrls.length > 0 ? `
### Media

${mediaUrls.map((url, i) => `${i + 1}. ${url}`).join('\n')}

` : ''}

${poll ? `
### Poll

Question: ${poll.question}
Options:
${poll.options.map((opt, i) => `  ${i + 1}. ${opt}`).join('\n')}
Duration: ${poll.durationMinutes} minutes

` : ''}

---

## Tweet Details

| Field | Value |
|-------|-------|
| Character Count | ${text?.length || 0} |
| Character Limit | ${isThread ? '25,000 (thread)' : '280'} |
| Media Count | ${mediaUrls?.length || 0} |
| Scheduled Time | ${scheduledTime || 'Immediate'} |
| Reply Settings | ${replySettings || 'Everyone'} |

---

## Thread Info

${isThread ? `
⚠️ **This is a thread** - Multiple connected tweets

` : '✅ Single tweet'}

---

## Preview

> ${text?.substring(0, 280) || 'No text'}${text?.length > 280 ? '...' : ''}
${mediaUrls?.length > 0 ? `\n📎 ${mediaUrls.length} media attachment(s)` : ''}

---

## Approval Required

⚠️ **This tweet will be saved as draft** on Twitter/X and will require manual review before publishing.

### Checklist

- [ ] Review content for accuracy and tone
- [ ] Check character count
- [ ] Verify media attachments
- [ ] Confirm reply settings
- [ ] Approve for publishing

---

## Execution Details

- **Operation:** Create Twitter/X Draft Tweet
- **API:** Twitter API v2
- **Endpoint:** /2/users/{id}/tweets
- **State:** draft

---

*Generated by Social Media MCP Server*
`;

    await writeFile(filepath, content, 'utf-8');

    return {
      filepath,
      filename,
      platform: 'twitter',
      type: isThread ? 'thread_draft' : 'tweet_draft'
    };
  }

  /**
   * Generate markdown for cross-platform summary
   */
  async generateCrossPlatformSummary(posts, options = {}) {
    await this.ensureDirectory();

    const timestamp = this.generateTimestamp();
    const filename = `CROSS_PLATFORM_SUMMARY_${timestamp}.md`;
    const filepath = join(this.outputDir, filename);

    const platformCounts = {
      facebook: posts.filter(p => p.platform === 'facebook').length,
      instagram: posts.filter(p => p.platform === 'instagram').length,
      twitter: posts.filter(p => p.platform === 'twitter').length
    };

    const totalPosts = Object.values(platformCounts).reduce((a, b) => a + b, 0);

    const content = `# 📊 Cross-Platform Social Media Summary

**Generated:** ${new Date().toISOString()}
**Status:** ⏳ Awaiting Approval
**Total Posts:** ${totalPosts}

---

## Overview

| Platform | Posts | Status |
|----------|-------|--------|
| Facebook | ${platformCounts.facebook} | ⏳ Pending |
| Instagram | ${platformCounts.instagram} | ⏳ Pending |
| Twitter/X | ${platformCounts.twitter} | ⏳ Pending |

---

## Posts Summary

${posts.map((post, index) => `
### ${index + 1}. ${post.platform.toUpperCase()} - ${post.type.replace('_', ' ')}

**Content Preview:**
> ${post.preview?.substring(0, 100) || 'No preview'}${post.preview?.length > 100 ? '...' : ''}

**File:** ${post.filename}

---
`).join('\n')}

## Approval Required

⚠️ **All posts above are saved as drafts** and require manual review before publishing.

### Bulk Actions

- [ ] Review all posts for brand consistency
- [ ] Check scheduling conflicts
- [ ] Verify cross-platform messaging alignment
- [ ] Approve all for publishing
- [ ] Schedule publishing times

---

## Campaign Details

${options.campaign ? `
**Campaign:** ${options.campaign}
` : ''}
${options.notes ? `
**Notes:** ${options.notes}
` : ''}

---

*Generated by Social Media MCP Server*
`;

    await writeFile(filepath, content, 'utf-8');

    return {
      filepath,
      filename,
      platform: 'all',
      type: 'cross_platform_summary'
    };
  }

  /**
   * Generate markdown for content calendar
   */
  async generateContentCalendar(posts, weekStart, options = {}) {
    await this.ensureDirectory();

    const timestamp = this.generateTimestamp();
    const filename = `CONTENT_CALENDAR_${weekStart}_${timestamp}.md`;
    const filepath = join(this.outputDir, filename);

    // Group posts by date
    const postsByDate = {};
    posts.forEach(post => {
      const date = post.scheduledDate || new Date().toISOString().split('T')[0];
      if (!postsByDate[date]) {
        postsByDate[date] = [];
      }
      postsByDate[date].push(post);
    });

    const content = `# 📅 Social Media Content Calendar

**Generated:** ${new Date().toISOString()}
**Week Starting:** ${weekStart}
**Status:** ⏳ Planning

---

## Weekly Overview

| Day | Facebook | Instagram | Twitter | Total |
|-----|----------|-----------|---------|-------|
${Object.keys(postsByDate).sort().map(date => {
  const dayPosts = postsByDate[date];
  const fb = dayPosts.filter(p => p.platform === 'facebook').length;
  const ig = dayPosts.filter(p => p.platform === 'instagram').length;
  const tw = dayPosts.filter(p => p.platform === 'twitter').length;
  return `| ${date} | ${fb} | ${ig} | ${tw} | ${dayPosts.length} |`;
}).join('\n')}

---

## Daily Schedule

${Object.keys(postsByDate).sort().map(date => `
### ${date}

${postsByDate[date].map((post, i) => `
${i + 1}. **${post.platform.toUpperCase()}** - ${post.type.replace('_', ' ')}
   - Preview: ${post.preview?.substring(0, 80) || 'No preview'}...
   - File: ${post.filename}
`).join('\n')}

`).join('\n')}

---

## Notes

${options.notes || '*No additional notes*'}

---

*Generated by Social Media MCP Server*
`;

    await writeFile(filepath, content, 'utf-8');

    return {
      filepath,
      filename,
      platform: 'all',
      type: 'content_calendar'
    };
  }
}

export default PendingApprovalGenerator;
